--Remove Duplicates

WITH RowNumCTE AS(
Select *,
	ROW_NUMBER() OVER (
	PARTITION BY ParcelID,
				PropertyAddress,
				SalePrice,
				SaleDate,
				LegalReference
				ORDER BY
					UniqueID
					) RowNum

From PortfolioProject..NashvilleHousing
--order by ParcelID
)
DELETE
From RowNumCTE
Where RowNum > 1
